#!/usr/bin/env node
/**
 * Generate Layer 1 API smoke specs from `crates/medbrains-server/src/routes/mod.rs`.
 *
 * For every parameter-less GET route, emits one Playwright assertion that
 * the endpoint returns 2xx + valid JSON. Endpoints whose path contains a
 * `{...}` parameter, or which only support POST/PUT/PATCH/DELETE, are
 * appended to `_skiplist.json` for human review (Layer 2 covers them).
 *
 * Output goes under `apps/web/e2e/smoke/api/<module>.smoke.spec.ts`.
 *
 * Run from repo root:
 *   node apps/web/scripts/generate-api-smoke.mjs
 */

import {
  readFileSync,
  writeFileSync,
  mkdirSync,
  rmSync,
  existsSync,
} from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "../../..");
const ROUTES_FILE = resolve(
  REPO_ROOT,
  "crates/medbrains-server/src/routes/mod.rs",
);
const OUTPUT_DIR = resolve(__dirname, "../e2e/smoke/api");
const SKIP_FILE = resolve(OUTPUT_DIR, "_skiplist.json");

function parseRoutes(source) {
  const routes = [];
  const stripped = source.replace(/\/\/[^\n]*/g, "");
  const re = /\.route\s*\(\s*"([^"]+)"\s*,\s*([\s\S]*?)\)\s*[\.;]/g;
  let m;
  while ((m = re.exec(stripped)) !== null) {
    const path = m[1];
    const handlerBlock = m[2];
    const methods = new Set();
    for (const verb of ["get", "post", "put", "patch", "delete"]) {
      const verbRe = new RegExp(`\\b${verb}\\s*\\(`, "g");
      if (verbRe.test(handlerBlock)) methods.add(verb.toUpperCase());
    }
    if (methods.size > 0) routes.push({ path, methods: [...methods] });
  }
  return routes;
}

function moduleOf(path) {
  const m = /^\/api\/([a-zA-Z0-9_-]+)/.exec(path);
  if (!m) return "_misc";
  return m[1].replace(/[^a-zA-Z0-9]/g, "_");
}

function specForModule(moduleName, paths) {
  const cases = paths
    .map((p) => `    { path: "${p}", desc: "${p}" },`)
    .join("\n");
  return `// AUTO-GENERATED by apps/web/scripts/generate-api-smoke.mjs — DO NOT EDIT.
// Regenerate with: node apps/web/scripts/generate-api-smoke.mjs
import { test, expect } from "@playwright/test";
import { E2E_BACKEND_URL, loginAsAdmin } from "../../helpers/api";

const cases: Array<{ path: string; desc: string }> = [
${cases}
];

test.describe("smoke ${moduleName}", () => {
  for (const { path, desc } of cases) {
    test(\`GET \${desc}\`, async ({ request }) => {
      const ctx = await loginAsAdmin(request);
      const resp = await ctx.request.fetch(\`\${E2E_BACKEND_URL}\${path}\`, {
        headers: { "x-csrf-token": ctx.csrfToken },
      });
      expect.soft(resp.status(), \`status for \${path}\`).toBeGreaterThanOrEqual(200);
      expect.soft(resp.status(), \`status for \${path}\`).toBeLessThan(500);
      const text = await resp.text();
      if (resp.headers()["content-type"]?.includes("application/json")) {
        try {
          JSON.parse(text);
        } catch {
          throw new Error(\`\${path} returned non-JSON body of length \${text.length}\`);
        }
      }
    });
  }
});
`;
}

function main() {
  const source = readFileSync(ROUTES_FILE, "utf8");
  const routes = parseRoutes(source);
  if (routes.length === 0) {
    console.error("no routes parsed — aborting");
    process.exit(1);
  }

  const smoke = new Map();
  const skipList = [];
  for (const r of routes) {
    const isParameterless = !r.path.includes("{");
    const supportsGet = r.methods.includes("GET");
    const isUnderApi = r.path.startsWith("/api/");
    if (!isUnderApi) continue;
    if (isParameterless && supportsGet) {
      const mod = moduleOf(r.path);
      if (!smoke.has(mod)) smoke.set(mod, new Set());
      smoke.get(mod).add(r.path);
    } else {
      skipList.push({
        path: r.path,
        methods: r.methods,
        reason: !isParameterless ? "has path param" : "no GET method",
      });
    }
  }

  if (existsSync(OUTPUT_DIR)) rmSync(OUTPUT_DIR, { recursive: true });
  mkdirSync(OUTPUT_DIR, { recursive: true });

  for (const [mod, paths] of smoke) {
    const sorted = [...paths].sort();
    const file = resolve(OUTPUT_DIR, `${mod}.smoke.spec.ts`);
    writeFileSync(file, specForModule(mod, sorted));
  }
  writeFileSync(SKIP_FILE, JSON.stringify(skipList, null, 2));

  const totalSmoke = [...smoke.values()].reduce((a, s) => a + s.size, 0);
  console.log(
    `generated ${smoke.size} smoke specs (${totalSmoke} GET endpoints) + ${skipList.length} skipped → ${OUTPUT_DIR}`,
  );
}

main();
